## ---------- If statement allow you to check specific condition  -----------------
#  != (not equal to)
#  -eq (equal to)
#  -ne (not equal to)
#  -gt (greater than)
#  -ge (greater than or equal to)
#  -lt (less than)
#  -le (les than or equal to)

if [ 50 -eq 100 ]   # --> this line check if the numer 50 is equal to the number 100
then 
    echo "The number 50 is equal to 100"
    else
    echo "The number 50 is not equal to 100"
fi

